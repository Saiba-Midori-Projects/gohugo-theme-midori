document.addEventListener('DOMContentLoaded', () => {
  const toc = document.querySelector('.post-toc');
  const tocToggle = toc?.querySelector('.post-toc__toggle');
  const tocToggleLabel = tocToggle?.querySelector('span');
  const tocLinks = toc ? Array.from(toc.querySelectorAll('.post-toc__nav a')) : [];

  const setToggleState = (isOpen) => {
    if (!toc || !tocToggle) {
      return;
    }

    toc.classList.toggle('is-open', isOpen);
    tocToggle.setAttribute('aria-expanded', String(isOpen));

    if (tocToggleLabel) {
      tocToggleLabel.textContent = isOpen ? '收起目录' : '展开目录';
    }
  };

  if (toc && tocToggle) {
    tocToggle.addEventListener('click', () => {
      setToggleState(!toc.classList.contains('is-open'));
    });
  }

  if (tocLinks.length === 0) {
    return;
  }

  const headingMap = tocLinks
    .map((link) => {
      const hash = link.getAttribute('href');

      if (!hash || !hash.startsWith('#')) {
        return null;
      }

      const target = document.getElementById(hash.slice(1));
      return target ? { link, target } : null;
    })
    .filter(Boolean);

  const setActiveLink = (activeId) => {
    headingMap.forEach(({ link, target }) => {
      link.classList.toggle('is-active', target.id === activeId);
    });
  };

  const observer = new IntersectionObserver(
    (entries) => {
      const visible = entries
        .filter((entry) => entry.isIntersecting)
        .sort((a, b) => a.boundingClientRect.top - b.boundingClientRect.top);

      if (visible.length > 0) {
        setActiveLink(visible[0].target.id);
      }
    },
    {
      rootMargin: '0px 0px -70% 0px',
      threshold: 0.1
    }
  );

  headingMap.forEach(({ target }) => observer.observe(target));

  tocLinks.forEach((link) => {
    link.addEventListener('click', () => {
      const hash = link.getAttribute('href');
      if (!hash) {
        return;
      }

      setActiveLink(hash.slice(1));
      if (toc?.classList.contains('is-open')) {
        setToggleState(false);
      }
    });
  });
});
